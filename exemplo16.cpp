#include<stdio.h>
#include<stdlib.h>

main()
{
	int cont,n; 

	for(cont=1;cont<=10;cont++){ //contador começando em 1 que vai até 10
	
		printf("==TABUADA DO %i ==\n",cont);
		for(n=1;n<=10;n++) //contador começando em 1 que vai até 10
		
		printf("%i X %i=%i  \n \n",cont,n,cont*n); //mascara sempre acompannha tudo
	

}	
	
	
	
}
